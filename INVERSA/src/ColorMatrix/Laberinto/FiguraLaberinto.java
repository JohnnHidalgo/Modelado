package ColorMatrix.Laberinto;

public class FiguraLaberinto {
    int width;
    int hegiht;

    public FiguraLaberinto(int width, int hegiht) {
        this.width = width;
        this.hegiht = hegiht;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHegiht() {
        return hegiht;
    }

    public void setHegiht(int hegiht) {
        this.hegiht = hegiht;
    }
}
